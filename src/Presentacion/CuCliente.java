    private void BTNMOSTRAR1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNMOSTRAR1ActionPerformed
        // TODO add your handling code here:
        try {
            String[][] tabla=cliente.ListaDeClientes();
            DefaultTableModel modelo= (DefaultTableModel) TCLIENTES.getModel();
            int filas=modelo.getRowCount();
            if (filas>0) {
                for (int i = 0; i < filas; i++) {
                    modelo.removeRow(0);
                }
            }
            for (int i = 0; i <tabla.length ; i++) {
                modelo.addRow(new Object[]{tabla[i][0],tabla[i][1],tabla[i][2],tabla[i][3]});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR DE SISTEMA "+e.getMessage());
        }
        
    }//GEN-LAST:event_BTNMOSTRAR1ActionPerformed
    
