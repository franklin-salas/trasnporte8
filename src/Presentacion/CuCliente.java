    private void BTNREGISTRARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNREGISTRARActionPerformed
        // TODO add your handling code here:
        String resutado="";
        try {
             ID=Integer.valueOf(TXTID.getText());
             if (TXTNOMBBRE.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "INTRODUZCA NOMBRE COMPLETO");
                return;
             }
             Nombre=TXTNOMBBRE.getText();
             if (CBSEXO.getSelectedIndex()==0) {
                JOptionPane.showMessageDialog(null, "SELECCIONE UN TIPO DE SEXO");
                return;
            }
             Sexo=CBSEXO.getSelectedItem().toString().substring(0, 1);
             if (TXTCI.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "INTRODUZCA CARNET DE IDENTIDAD");
                return;
             }
             CI=Integer.valueOf(TXTCI.getText());
             resutado=cliente.insertar(ID, CI, Nombre, Sexo);
             JOptionPane.showMessageDialog(null, resutado);
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null,resutado+" "+ e.getMessage());
        }

    }//GEN-LAST:event_BTNREGISTRARActionPerformed

