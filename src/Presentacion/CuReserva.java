    private void BTNMOSTRAR1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNMOSTRAR1ActionPerformed
        // TODO add your handling code here:
        try {
            String fecha=JOptionPane.showInputDialog(null,"INTRODUZCA LA FECHA DE LAS RESERVAS");
            String[][] table=reserva.ListaDeReservas("'"+fecha+"'");
            DefaultTableModel modelo= (DefaultTableModel) TBOLETOS.getModel();
            int Tfila=modelo.getRowCount();
            if (Tfila>0) {
                for (int i = 0; i <Tfila ; i++) {
                    modelo.removeRow(0);
                }
            }
            int tuplas=table.length;
            for (int i = 0; i < tuplas; i++) {
                modelo.addRow(new Object[]{table[i][0],table[i][1],table[i][2],table[i][3]});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "DATOS INVALIDOS "+e.getMessage());
        }
    }//GEN-LAST:event_BTNMOSTRAR1ActionPerformed

