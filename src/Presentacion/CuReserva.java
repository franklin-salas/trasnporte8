    private void BTNBUSCARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNBUSCARActionPerformed
        try {
            if (TXTBOLETO.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "INTRODUZCA CODIGO DE BOLETO");
                return;
            }
            String[] resultado=reserva.Consultabuscar(TXTBOLETO.getText());
            TXTCOD.setText(resultado[0]);
            TXTFECHA.setText(resultado[1]);
            TXTHORA.setText(resultado[2]);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NO EXISTE RESERVA PARA EL BOLETO");
        }
    }//GEN-LAST:event_BTNBUSCARActionPerformed

